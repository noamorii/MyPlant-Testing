            <h2> Geranium</h2>
            <p>
                Geraniums are a long time favorite of gardeners. They are easy to grow, colorful, and emit a lovely scent. Varieties of colors available in shades of white, red, pink, purple, mauve, orange, and reddish-black. You don’t need to be a master gardener to know how to grow geraniums. Geraniums care little for fancy fertilizers or specialized soils, and they require just a bit of basic care to thrive.
            </p>
            <p>
                Geraniums may be grown as houseplants or as annual flowers. During the warmer months of the year, they can be kept outdoors in a sunny location. The perfect soil for geraniums is a loose soil with plenty of organic matter. If your soil is on the heavy side, incorporate peat, compost or perlite. Manure and vermiculite are not recommended.
            </p>

