            <h2> Azalea </h2>
            <p>
                Azaleas are hard to beat if you’re looking for a low-maintenance plant that produces masses of bright color and attractive foliage. Some deciduous types produce gorgeous autumn colors, while evergreen varieties add year-round interest to the garden. Neat and compact, azaleas are well-suited for container growing.
            </p>
            <p>
                Water azalea in planters whenever the top of the soil feels dry to the touch. Check your plant daily during hot, dry weather; potted azaleas dry quickly. Use rainwater, if possible, as rainwater is less alkaline than tap water. Feed the plants every month between spring and late summer, using a fertilizer formulated for acid-loving plants. Alternatively, use a slow-release fertilizer a couple of times during the season.
            </p>
