            <h2> Echinopsis cactus </h2>
            <p>
                Echinopsis species are hardy plants that grow well like many cacti and succulents while being left alone. They are popular due to their size and unique appearance of flowers. The large columnar cactus is beautiful, but it is far too large for indoor cultivation, so only the smaller day-flowering species are grown indoors and are now classified in the Echinopsis genus.
            </p>
